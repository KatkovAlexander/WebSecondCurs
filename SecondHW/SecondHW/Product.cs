namespace SecondHW
{
    public class Product
    {
        public int ProductId;
        public int CategoryId;
        public int BrandId;
        public string ProductColor;
        public int ProductAmount;

        public Product(int prId, int catId, int brId, string prColor, int prAmount)
        {
            ProductId = ProductId;
            CategoryId = catId;
            BrandId = brId;
            ProductColor = prColor;
            ProductAmount = prAmount;
        }
    }
}